def build_user_prompt(input_text: str, genre: str, keywords: list[str]) -> str:
    """学習データのuser roleプロンプトを構築"""
    style_hint = GENRE_STYLE_HINTS.get(genre, "")
    kw_str = ", ".join(keywords) if keywords else "（自動抽出）"
    prompt = f"以下の学習テキストを{genre}ジャンルの歌詞にしてください。\n"
    if style_hint:
        prompt += f"スタイル: {style_hint}\n"
    prompt += f"重要キーワード: {kw_str}\n\n"
    prompt += f"学習テキスト:\n{input_text}"
    return prompt


def build_training_record(example: dict) -> dict:
    """スタイル例 → SFT学習レコード (messages形式)"""
    return {
        "messages": [
            {"role": "system", "content": SYSTEM_PROMPT},
            {
                "role": "user",
                "content": build_user_prompt(
                    example["input_text"],
                    example["genre"],
                    example["keywords"],
                ),
            },
            {"role": "assistant", "content": example["lyrics"]},
        ]
    }


def get_all_template_records() -> list[dict]:
    """全テンプレートを学習レコードに変換"""
    return [build_training_record(ex) for ex in STYLE_EXAMPLES]
