# ジャンルをランダムに割り振る
GENRES = ["pop", "rock", "hip-hop", "EDM"]

INSTRUCTION_TEMPLATE = (
    "あなたは暗記学習用の歌詞作成の専門家です。以下の学習テキストから{genre}ジャンルの歌詞を作成してください。\n"
    "エグスプロージョンの「本能寺の変」のようにユーモアたっぷりでキャッチー、でも学習内容は正確に盛り込んでください。\n"
    "韻を踏み、合いの手や掛け声も入れて覚えやすくしてください。\n"
    "重要な用語・人物名・年号は必ず歌詞に含めてください。\n"
    "出力は [Verse 1], [Chorus], [Verse 2] 等のセクションラベル付きの歌詞のみにしてください。"
)


def generate_with_gemini(topics, api_key):
    """Gemini APIで歌詞を生成"""
    try:
        import google.generativeai as genai
    except ImportError:
        logger.error("pip install google-generativeai が必要です")
        sys.exit(1)

    genai.configure(api_key=api_key)
    model = genai.GenerativeModel("gemini-2.5-flash-lite")
    request_options = {"timeout": 60}

    results = []
    total = len(topics)

    for i, topic in enumerate(topics):
        genre = GENRES[i % len(GENRES)]
        instruction = INSTRUCTION_TEMPLATE.format(genre=genre)

        prompt = f"{instruction}\n\n学習テキスト:\n{topic['input']}"

        try:
            response = model.generate_content(prompt, request_options=request_options)
            lyrics = response.text.strip()

            # 基本チェック: セクションラベルがあるか
            if "[Verse" not in lyrics and "[Chorus" not in lyrics:
                logger.warning(f"[{i+1}/{total}] セクションラベルなし、スキップ")
                continue

            record = {
                "instruction": instruction,
                "input": topic["input"],
                "output": lyrics,
            }
            results.append(record)
            logger.info(f"[{i+1}/{total}] OK: {topic['input'][:40]}... ({genre})")

        except Exception as e:
            logger.warning(f"[{i+1}/{total}] 生成失敗: {e}")

        # API レート制限対策
        time.sleep(2)

    return results


